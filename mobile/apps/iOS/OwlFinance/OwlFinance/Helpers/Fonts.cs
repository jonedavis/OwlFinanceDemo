using System;
using System.Collections.Generic;
using System.Text;
using UIKit;

namespace OwlFinance.Helpers
{
    class Fonts
    {
        public static UIFont Regular(nfloat size)
        {
            return UIFont.FromName("Lato-Regular", size);
        }

        public static UIFont Bold(nfloat size)
        {
            return UIFont.FromName("Lato-Regular", size);
        }
    }
}
